<p><b>Examples of Bad Headings</b></p>
<table class="good-bad-example-table">
  <tr>
    <th>Example</th>
    <th>Why this is bad</th>
  </tr>
  <tr>
    <td>&lt;h1&gt;Welcome&lt;/h1&gt;</td>
    <td>Too generic. It provides no context about the page topic.</td>
  </tr>
  <tr>
    <td>&lt;h2&gt;Travel Deals Flights and Hotels&lt;/h2&gt;</code></td>
    <td>Keyword stuffing makes the heading hard to read and untrustworthy.</td>
  </tr>
  <tr>
    <td>&lt;h4&gt;Online Courses&lt;/h4&gt;(used as the first heading on the page)</td>
    <td>Incorrect heading hierarchy. Pages should start with an H1.</td>
  </tr>
  <tr>
    <td>&lt;h1&gt;Best Laptops&lt;/h1&gt; &lt;h1&gt;Cheap Laptops&lt;/h1&gt;</td>
    <td>Multiple H1s targeting different intents confuse users and search engines.</td>
  </tr>
  <tr>
    <td>&lt;h2&gt;Technology&lt;/h2&gt;</td>
    <td>Too short and vague. It doesn’t explain what aspect of technology is being discussed.</td>
  </tr>
  <tr>
    <td>&lt;div class="big-text"&gt;Hotel Information&lt;/div&gt;</code></td>
    <td>Styled text instead of a real heading element, offering no structural or SEO value.</td>
  </tr>
</table>

<p>Well written headings help users navigate content effortlessly and give search engines clear signals about topic relevance. Always prioritize clarity, structure, and intent over visual styling or keyword overload in the content of heading tags.</p>


