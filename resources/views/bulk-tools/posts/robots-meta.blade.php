                  <h2 class="tools_des_fastheading">How to pass the Robot Meta Tag test?</h2>
                  <p>This document details how the page- and text-level settings can be used to adjust how Google presents your content in search results. You can specify page-level settings by including a meta tag on HTML pages or in an HTTP header. You can specify text-level settings with the data-nosnippet attribute on HTML elements within a page.

                    Keep in mind that these settings can be read and followed only if crawlers are allowed to access the pages that include these settings.</p>
                  
                  
